strdata = 'Time is money!!'
print(strdata[1:5])   #'ime'가 출력됨
print(strdata[:7])    #'Time is'가 출력됨
print(strdata[9:])    #'oney!!'가 출력됨
print(strdata[:-3])   #'Time is mone'이 출력됨
print(strdata[-3:])   #'y!!'이 출력됨
print(strdata[:])     #'Time is money!!'가 출력됨
